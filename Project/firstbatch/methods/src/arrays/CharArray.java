package arrays;

class CharArray {

	public static void main(String[] args) {
		char[] ch=new char[5];
		ch[0]='n';
		ch[1]='A';
		ch[2]='$';
		ch[3]='&';
		ch[4]='!';
		System.out.println(ch[0]);
		System.out.println(ch[1]);
		System.out.println(ch[2]);
		System.out.println(ch[3]);
		System.out.println(ch[4]);
				
	}

}
